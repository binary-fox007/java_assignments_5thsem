public class assignment9 {
    // write a java program to show the left shift and right shift operation
    public static void main(String[] args) {
        int num = 12;
        int leftshifted = num << 2;
        System.out.println("left shifted value is" + leftshifted);
        int rightshifted = num >> 2;
        System.out.println("left shifted value is" + rightshifted);
    }
}
